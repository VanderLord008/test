window.addEventListener("keydown",function(evt)
{
    if(evt.key==="a")
    {
        let key1=this.document.querySelector(".keyA");
        let aud1=this.document.querySelector("#audA");
        key1.classList.add("funA");
        aud1.play();
    }
})

window.addEventListener("keyup",function(evt)
{
    if(evt.key==="a")
    {
        let key1=this.document.querySelector(".keyA");
        
        key1.classList.remove("funA");
        
    }
})



